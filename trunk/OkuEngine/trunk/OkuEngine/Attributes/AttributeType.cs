using System;

namespace OkuEngine.Attributes
{
  /// <summary>
  /// Defines the type of an attribute.
  /// </summary>
  public enum AttributeType
  {
    /// <summary>
    /// A boolean value.
    /// </summary>
    Boolean,
    /// <summary>
    /// An integer value.
    /// </summary>
    Integer,
    /// <summary>
    /// A float value.
    /// </summary>
    Number,
    /// <summary>
    /// A string value.
    /// </summary>
    String
  }
}